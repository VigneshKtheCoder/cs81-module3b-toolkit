function double(n) {
    return n * 2;
}
console.log(double(4));


function square(a) {
    return a**2;
}
console.log(square(9));

function isEven(e) {
    return e % 2 == 0;
}
console.log(isEven(4));

function isOdd(o) {
    return o % 2 != 0;
}
console.log(isOdd(9));

function multiply(a,b) {
    return a*b;
}
console.log(multiply(5,6));

function cube(c) {
    return c**3;
}
console.log(cube(5));
